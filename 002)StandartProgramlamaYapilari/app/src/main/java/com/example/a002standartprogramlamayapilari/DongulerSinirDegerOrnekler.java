package com.example.a002standartprogramlamayapilari;

public class DongulerSinirDegerOrnekler {
    public static void main(String[] args) {
        for (int k = 3; k <= 7;k++){
            System.out.println("K değer: " + k);
        }

        int a = 3;
        while (a <= 7){
            System.out.println("A değer: " + a);
            a++;
        }

        for (int z = 0;z <= 8;z+=2){
            System.out.println("Z değer: " + z);
        }

        int m = 0;
        while (m <= 9){
            System.out.println("M değer: " + m);
            m+=2;
        }
    }
}
